// ==========================================
// Instalaci√≥n del Service Worker
// ==========================================
self.addEventListener('install', (event) => {
    console.log('üîß Service Worker: Instalando (MODO DESARROLLO - SIN CACH√â)...');
    
    event.waitUntil(
        // NO precachear nada, ir directo a activaci√≥n
        Promise.resolve()
            .then(() => {
                console.log('‚úÖ Service Worker: Instalaci√≥n completada (sin precache)');
                return self.skipWaiting();
            })
            .catch(error => {
                console.error('‚ùå Error durante la instalaci√≥n:', error);
            })
    );
});

// ==========================================
// Activaci√≥n del Service Worker
// ==========================================
self.addEventListener('activate', (event) => {
    console.log('üéØ Service Worker: Activando...');
    
    event.waitUntil(
        caches.keys().then(cacheNames => {
            return Promise.all(
                cacheNames.map(cacheName => {
                    // Eliminar cach√©s antiguos
                    if (cacheName !== CACHE_NAME && cacheName !== RUNTIME_CACHE) {
                        console.log('üóëÔ∏è Service Worker: Eliminando cach√© antiguo:', cacheName);
                        return caches.delete(cacheName);
                    }
                })
            );
        })
        .then(() => {
            console.log('‚úÖ Service Worker: Activaci√≥n completada');
            // ELIMINAR TODOS LOS CACH√âS SIN EXCEPCI√ìN
            return caches.keys().then(cacheNames => {
                return Promise.all(
                    cacheNames.map(cacheName => {
                        console.log('üóëÔ∏è ELIMINANDO cach√©:', cacheName);
                        return caches.delete(cacheName);
                    })
                );
            });
        })
        .then(() => {
            console.log('üîÑ Service Worker: Todos los cach√©s limpiados, forzando actualizaci√≥n completa');
            // Notificar a todos los clientes que se actualice
            return self.clients.claim();
        })
        .then(() => {
            // Enviar mensaje a todos los clientes para que recarguen
            return self.clients.matchAll();
        })
        .then(clients => {
            clients.forEach(client => {
                client.postMessage({
                    type: 'SW_UPDATED',
                    message: 'Service Worker actualizado. Recargando p√°gina...'
                });
            });
        })
    );
});

// ==========================================
// Intercepci√≥n de Peticiones (Fetch)
// ==========================================
self.addEventListener('fetch', (event) => {
    const { request } = event;
    const url = new URL(request.url);

    // Estrategia: SIEMPRE RED PRIMERO para desarrollo
    if (url.origin === location.origin) {
        // Para TODOS los archivos locales, siempre red primero (sin cach√©)
        event.respondWith(alwaysNetworkFirst(request));
    }
    // Estrategia: Network First para tiles de mapas
    else if (url.hostname.includes('tile.openstreetmap.org')) {
        event.respondWith(networkFirst(request));
    }
    // Estrategia: Stale While Revalidate para CDN
    else if (url.hostname.includes('unpkg.com') || 
             url.hostname.includes('cdnjs.cloudflare.com')) {
        event.respondWith(staleWhileRevalidate(request));
    }
    // Default: Network First
    else {
        event.respondWith(networkFirst(request));
    }
});

// ==========================================
// Estrategia: Cache First
// Intenta desde cach√©, si falla va a red
// ==========================================
async function cacheFirst(request) {
    const cachedResponse = await caches.match(request);
    
    if (cachedResponse) {
        console.log('üì¶ Desde cach√©:', request.url);
        return cachedResponse;
    }

    try {
        console.log('üåê Desde red:', request.url);
        const networkResponse = await fetch(request);
        
        // Cachear la nueva respuesta
        if (networkResponse && networkResponse.status === 200) {
            const cache = await caches.open(CACHE_NAME);
            cache.put(request, networkResponse.clone());
        }
        
        return networkResponse;
    } catch (error) {
        console.error('‚ùå Error en fetch:', error);
        
        // Retornar p√°gina offline si existe
        return caches.match('/index.html');
    }
}

// ==========================================
// Estrategia: Network First
// Intenta desde red, si falla usa cach√©
// ==========================================
async function networkFirst(request) {
    try {
        const networkResponse = await fetch(request);
        
        // Cachear en runtime
        if (networkResponse && networkResponse.status === 200) {
            const cache = await caches.open(RUNTIME_CACHE);
            cache.put(request, networkResponse.clone());
        }
        
        return networkResponse;
    } catch (error) {
        console.log('üåê‚û°Ô∏èüì¶ Red fall√≥, intentando cach√©:', request.url);
        const cachedResponse = await caches.match(request);
        
        if (cachedResponse) {
            return cachedResponse;
        }
        
        throw error;
    }
}

// ==========================================
// Estrategia: SIEMPRE RED PRIMERO (Para desarrollo)
// NUNCA usa cach√© para archivos locales, siempre red
// ==========================================
async function alwaysNetworkFirst(request) {
    try {
        // SIEMPRE intentar red primero, SIN cach√©
        const networkResponse = await fetch(request, {
            cache: 'no-store', // NUNCA usar cach√© del navegador
            headers: {
                'Cache-Control': 'no-cache, no-store, must-revalidate, max-age=0',
                'Pragma': 'no-cache',
                'Expires': '0'
            }
        });
        
        if (networkResponse && networkResponse.status === 200) {
            console.log('üåê SIEMPRE desde red (sin cach√©):', request.url);
            return networkResponse;
        }
        
        throw new Error('Respuesta de red no v√°lida');
        
    } catch (error) {
        console.log('‚ùå Red fall√≥ completamente:', request.url, error);
        
        // SOLO como √∫ltimo recurso, intentar cach√©
        const cachedResponse = await caches.match(request);
        if (cachedResponse) {
            console.log('üö® √öLTIMO RECURSO - Desde cach√©:', request.url);
            return cachedResponse;
        }
        
        // Si es p√°gina HTML y no hay nada, retornar p√°gina principal
        if (request.url.includes('.html') || request.url === '/') {
            const fallbackResponse = await caches.match('/index.html');
            if (fallbackResponse) {
                return fallbackResponse;
            }
        }
        
        throw error;
    }
}

