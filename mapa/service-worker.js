// Estrategia: Network First con Fallback Agresivo
// Siempre intenta red primero, fallback a cach√© solo si es cr√≠tico
// ==========================================
async function networkFirstWithFallback(request) {
    try {
        // Intentar red primero con timeout corto
        const controller = new AbortController();
        const timeoutId = setTimeout(() => controller.abort(), 3000); // 3 segundos timeout
        
        // Agregar timestamp para evitar cach√© del navegador
        const urlWithTimestamp = new URL(request.url);
        urlWithTimestamp.searchParams.set('v', Date.now());
        
        const networkResponse = await fetch(urlWithTimestamp.toString(), { 
            signal: controller.signal,
            cache: 'no-cache', // Forzar no usar cach√© del navegador
            headers: {
                'Cache-Control': 'no-cache, no-store, must-revalidate',
                'Pragma': 'no-cache',
                'Expires': '0'
            }
        });
        
        clearTimeout(timeoutId);
        
        // Si la respuesta es exitosa, cachear y retornar
        if (networkResponse && networkResponse.status === 200) {
            const cache = await caches.open(CACHE_NAME);
            cache.put(request, networkResponse.clone());
            console.log('üåê Servido desde red (actualizado):', request.url);
            return networkResponse;
        }
        
        // Si la respuesta no es exitosa, intentar cach√©
        const cachedResponse = await caches.match(request);
        if (cachedResponse) {
            console.log('üì¶ Red fall√≥, sirviendo desde cach√©:', request.url);
            return cachedResponse;
        }
        
        throw new Error('No hay respuesta de red ni cach√©');
        
    } catch (error) {
        console.log('üåê‚û°Ô∏èüì¶ Red fall√≥, intentando cach√©:', request.url);
        const cachedResponse = await caches.match(request);
        
        if (cachedResponse) {
            console.log('üì¶ Sirviendo desde cach√© (offline):', request.url);
            return cachedResponse;
        }
        
        // Para archivos cr√≠ticos, retornar p√°gina principal
        if (request.url.includes('.html') || request.url === '/') {
            const fallbackResponse = await caches.match('/index.html');
            if (fallbackResponse) {
                return fallbackResponse;
            }
        }
        
        throw error;
    }
}

// ==========================================
// Estrategia: Stale While Revalidate
// Retorna cach√© inmediatamente y actualiza en background
// ==========================================
async function staleWhileRevalidate(request) {
    const cachedResponse = await caches.match(request);
    
    const fetchPromise = fetch(request).then(networkResponse => {
        if (networkResponse && networkResponse.status === 200) {
            const cache = caches.open(RUNTIME_CACHE);
            cache.then(c => c.put(request, networkResponse.clone()));
        }
        return networkResponse;
    }).catch(error => {
        console.log('Error en background fetch:', error);
    });

    return cachedResponse || fetchPromise;
}

